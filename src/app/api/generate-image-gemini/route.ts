import { NextRequest, NextResponse } from 'next/server';

export async function POST(request: NextRequest) {
  const keys = [
    process.env.GEMINI_API_KEY,
    process.env.GEMINI_API_KEY_2,
    process.env.GEMINI_API_KEY_3,
    process.env.GEMINI_API_KEY_4,
  ]
    .map((k) => k?.trim())
    .filter(Boolean) as string[];

  if (keys.length === 0) {
    return NextResponse.json({ error: 'Gemini API key not configured' }, { status: 500 });
  }

  const formData = await request.formData();
  const prompt = formData.get('prompt') as string;
  const images = formData.getAll('image[]') as File[];
  const keyIndexRaw = formData.get('keyIndex') as string | null;
  const requestedKeyIndex = keyIndexRaw ? Number.parseInt(keyIndexRaw, 10) : NaN;
  const keyIndex = Number.isFinite(requestedKeyIndex) ? requestedKeyIndex : -1;

  // Convert uploaded images to base64 inline_data parts
  const imageParts = await Promise.all(
    images.map(async (img) => {
      const buffer = Buffer.from(await img.arrayBuffer());
      const base64 = buffer.toString('base64');
      const mimeType = img.type || 'image/jpeg';
      return {
        inline_data: {
          mime_type: mimeType,
          data: base64,
        },
      };
    })
  );

  const body = {
    contents: [
      {
        role: 'user',
        parts: [
          // Reference images first, then the prompt.
          ...imageParts,
          { text: prompt },
        ],
      },
    ],
    generationConfig: {
      responseModalities: ['TEXT', 'IMAGE'],
      imageConfig: {
        // Wizard uses square previews by default
        aspectRatio: '1:1',
        imageSize: '512',
      },
    },
  };

  // "Nano Banana" image generation models (Gemini API)
  // https://ai.google.dev/gemini-api/docs/image-generation
  const preferredModel =
    process.env.GEMINI_IMAGE_MODEL?.trim() ||
    'gemini-2.5-flash-image';

  const modelFallbacks = [
    preferredModel,
    'gemini-3.1-flash-image-preview',
    'gemini-3-pro-image-preview',
  ];

  const pickKey = (idx: number) => {
    if (keys.length === 0) return '';
    if (idx >= 0 && idx < keys.length) return keys[idx];
    // deterministic-ish spread when caller doesn't specify a keyIndex
    return keys[Math.abs(Date.now()) % keys.length];
  };

  const callModel = async (model: string, apiKeyToUse: string) => {
    const url = `https://generativelanguage.googleapis.com/v1beta/models/${encodeURIComponent(model)}:generateContent`;
    const controller = new AbortController();
    const timeout = setTimeout(() => controller.abort(), 60_000);
    try {
      return await fetch(url, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'x-goog-api-key': apiKeyToUse,
        },
        body: JSON.stringify(body),
        signal: controller.signal,
      });
    } finally {
      clearTimeout(timeout);
    }
  };

  let resp: Response | null = null;
  let lastErrText = '';
  let lastStatus = 500;
  let usedModel = preferredModel;
  let usedKeyIndex = keyIndex;

  for (const model of modelFallbacks) {
    usedModel = model;
    const apiKeyToUse = pickKey(usedKeyIndex);
    resp = await callModel(model, apiKeyToUse);
    lastStatus = resp.status;
    if (resp.ok) break;
    lastErrText = await resp.text().catch(() => '');
    // try next model on "not found" / "not supported"
    if (resp.status === 404 || resp.status === 400) continue;
    break;
  }

  // Retry once on rate limit (rotate key)
  if (resp && resp.status === 429) {
    await new Promise((r) => setTimeout(r, 10_000));
    usedKeyIndex = keys.length > 0 ? (Math.max(usedKeyIndex, 0) + 1) % keys.length : usedKeyIndex;
    resp = await callModel(usedModel, pickKey(usedKeyIndex));
  }

  if (!resp || !resp.ok) {
    const errText = resp ? (await resp.text().catch(() => lastErrText)) : lastErrText;
    return NextResponse.json(
      {
        error: errText || 'Gemini request failed',
        model: usedModel,
        status: resp?.status ?? lastStatus,
      },
      { status: resp?.status ?? lastStatus }
    );
  }

  const data = await resp.json();

  // Extract the generated image from Gemini's response
  const candidates = data.candidates || [];
  for (const candidate of candidates) {
    const parts = candidate.content?.parts || [];
    for (const part of parts) {
      const inline = part.inline_data || part.inlineData;
      if (inline) {
        // Return in the same format as the OpenAI route for compatibility
        return NextResponse.json({
          data: [{ b64_json: inline.data }],
          model: usedModel,
        });
      }
    }
  }

  return NextResponse.json(
    { error: 'No image generated by Gemini', raw: data, model: usedModel },
    { status: 500 }
  );
}
